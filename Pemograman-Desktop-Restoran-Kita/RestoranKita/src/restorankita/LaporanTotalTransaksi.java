    private void hitungTotal() {
        //To change body of generated methods, choose Tools | Templates.
        int jumBaris = tblHasilPenjualan.getRowCount();
        int tot = 0;
        for (int i=0; i<jumBaris; i++){
            // ambil data total dari grid (kolom ke 4)
            int nJum = Integer.parseInt((String) tblHasilPenjualan.getValueAt(i,2));
            tot += nJum;
        }
        txtTotalPenjualan.setText(Integer.toString(tot));
        //tampilkan di display dengan pemisah ribuan
        /*DecimalFormat ribuan = new DecimalFormat("###,###");
        txtJumlahBayar.setText(ribuan.format(tot));*/
        
    }

