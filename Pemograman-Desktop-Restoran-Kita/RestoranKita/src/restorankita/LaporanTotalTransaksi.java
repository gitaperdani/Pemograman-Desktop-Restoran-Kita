    private void btnTampilSemuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTampilSemuaActionPerformed
        // TODO add your handling code here:
        model.setRowCount(0);
        
        String sql = "select * from transaksi";
        
       

        try {
            stmt = con.createStatement();
            rs = stmt.executeQuery(sql); //proses query
            
            //mengambil data satu persatu dari database
            while (rs.next()) { 
                NoTransaksi = rs.getString("notransaksi");
                Tanggal= rs.getString("tanggal");
                JumlahBayar = rs.getString("jumlahbayar");
                
                
                //memasukkan data dari database ke JTable
                String data[] = {NoTransaksi, Tanggal, JumlahBayar};
                model.addRow(data);
            }

        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTampilSemuaActionPerformed

