    private void txtKodeMenuKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKodeMenuKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            String sql = "select * from menu where kodemenu LIKE '%" + txtKodeMenu.getText()+"%' ";
            try {
                stmt = con.createStatement();
                rs = stmt.executeQuery(sql); //proses query

                while (rs.next()) {
                    txtNamaMenu.setText(rs.getString("namamenu"));
                    txtNamaMakanMinum.setText(rs.getString("namamakanan_minuman"));
                    txtHarga.setText(rs.getString("harga"));

                }
            } catch (Exception e) {
                System.out.println("Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_txtKodeMenuKeyPressed

