   private void kodemenu() {
        try {
            String query = "SELECT * FROM menu ORDER BY kodemenu DESC";
            stmt = con.createStatement();
            rs = stmt.executeQuery(query);
            if (rs.next()) {
                String kode = rs.getString("kodemenu").substring(1);
                String angka = "" + (Integer.parseInt(kode) + 1);
                String nol = "";

                switch (angka.length()) {
                    case 1:
                        nol = "00";
                        break;
                    case 2:
                        nol = "0";
                        break;
                    case 3:
                        nol = "";
                        break;
                    default:
                        break;
                }

                txtKodeMenu.setText("M" + nol + angka);
            }

                
        } catch (SQLException se) {
            JOptionPane.showMessageDialog(null, se.getMessage());
        }
    } 
